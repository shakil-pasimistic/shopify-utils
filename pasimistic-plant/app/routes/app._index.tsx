export const loader = async ({ request }: LoaderFunctionArgs) => {
  const { admin } = await authenticate.admin(request);

  const items = await admin.graphql(
    `#graphql
      query {
        products(first: 10) {
          edges {
            node {
              title
            }
          }
        }
      }`,
  );

  const productsJson = await items.json();

  const products = productsJson.data?.products?.edges.map(
    (product: { node: { title: string } }) => {
      return product.node.title;
    },
  );

  return json({
    products,
  });
};

export const action = async ({ request }: ActionFunctionArgs) => {
  const { admin } = await authenticate.admin(request);
  const color = ["Red", "Orange", "Yellow", "Green"][
    Math.floor(Math.random() * 4)
  ];

  const items = await admin.graphql(
    `#graphql
      query {
        products(first: 10) {
          edges {
            node {
              title
            }
          }
        }
      }`,
  );

  const productsJson = await items.json();

  const products = productsJson.data?.products?.edges.map(
    // eslint-disable-next-line array-callback-return
    (product: { node: { title: string } }) => {
      return product.node.title;
    },
  );

  console.log("ðŸš€ ~ action ~ products:", products);

  const response = await admin.graphql(
    `#graphql
      mutation populateProduct($input: ProductInput!) {
        productCreate(input: $input) {
          product {
            id
            title
            handle
            status
            variants(first: 10) {
              edges {
                node {
                  id
                  price
                  barcode
                  createdAt
                }
              }
            }
          }
        }
      }`,
    {
      variables: {
        input: {
          title: `${color} Snowboard`,
        },
      },
    },
  );
  const responseJson = await response.json();

  const variantId =
    responseJson.data!.productCreate!.product!.variants.edges[0]!.node!.id!;
  const variantResponse = await admin.graphql(
    `#graphql
      mutation shopifyRemixTemplateUpdateVariant($input: ProductVariantInput!) {
        productVariantUpdate(input: $input) {
          productVariant {
            id
            price
            barcode
            createdAt
          }
        }
      }`,
    {
      variables: {
        input: {
          id: variantId,
          price: Math.random() * 100,
        },
      },
    },
  );

  const variantResponseJson = await variantResponse.json();

  return json({
    product: responseJson!.data!.productCreate!.product,
    variant: variantResponseJson!.data!.productVariantUpdate!.productVariant,
    products,
  });
};

